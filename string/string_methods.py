name = "ANONymOUS DudE"

#1. lower() method
print(name.lower())#output---- anonymous dude

#2. upper() method
print(name.upper())#output ----ANONYMOUS DUDE

#3. count() method ----- will count single character
print(name.count("d"))#output-----

#4. title() method
print(name.title())#output--- Anonymous Dude

#5. len() 
print(len(name))
